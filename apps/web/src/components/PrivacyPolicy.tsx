/**
 * GDPR Art. 13/14 Privacy Policy — standalone document.
 *
 * Separate from Terms (LegalDisclaimer.tsx) by regulatory design:
 * GDPR requires the privacy notice to be its own surface so users
 * can find it without wading through ToS. We open it via a dedicated
 * footer link, with the same modal pattern as Terms.
 *
 * No ack checkbox here — Privacy Policy is informational, not
 * accept-or-decline. The user's right to be informed is satisfied
 * by us making the text available; they don't sign anything.
 *
 * If we ever change the practical processing (e.g. add analytics,
 * change retention period, add a sub-processor), the policy text
 * must be updated AND a notice posted to existing users at next
 * sign-in. For now the doc is intentionally short because we collect
 * very little.
 */

import { useState } from 'react';

interface Props {
  /** Render the trigger link only; the modal is mounted lazily on
   *  first open and stays in memory after to keep re-opens snappy. */
  triggerLabel?: string;
}

export function PrivacyPolicy({ triggerLabel = 'Privacy Policy' }: Props) {
  const [open, setOpen] = useState(false);
  return (
    <>
      <button
        type="button"
        onClick={() => setOpen(true)}
        className="text-cyan-400 underline-offset-2 hover:text-cyan-300 hover:underline"
      >
        {triggerLabel}
      </button>
      {open && <PrivacyModal onClose={() => setOpen(false)} />}
    </>
  );
}

