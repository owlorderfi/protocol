function Section({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <div>
      <h3 className="mb-1 text-sm font-semibold uppercase tracking-wider text-cyan-300/90">
        {title}
      </h3>
      <div className="text-sm text-slate-300">{children}</div>
    </div>
  );
}
